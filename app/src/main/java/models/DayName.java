public enum DayName {
	/***********************************************************************************************
	 * GLOBAL VARIABLES
	 **********************************************************************************************/
	/****/
	MON("Monday"),
	TUE("Tuesday"),
	WED("Wednesday"),
	THU("Thursday"),
	FRI("Friday"),
	SAT("Saturday"),
	SUN("Sunday");

	/***********************************************************************************************
	 * CONSTRUCTORS
	 **********************************************************************************************/
	String dayName;
	DayName(String dayName) {this.dayName = dayName;}

	/***********************************************************************************************
	 * OVERRIDE METHODS
	 **********************************************************************************************/
	@Override
	public String toString() {
		return this.dayName;
	}
}
